Cypress.Commands.overwrite('request', (originalFn, options) => {
    options.url = Cypress.config('baseUrl')
    options.headers = {
        Authorization: `Bearer ${token.token}`,
        contentType: 'application/json',
    };
  
    return originalFn(options);
  });